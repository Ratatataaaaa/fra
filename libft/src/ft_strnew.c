#include "libft.h"
#include <stdlib.h>

char		*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	if ((str = (char *)malloc(sizeof(*str) * (size + 1))) == 0)
		return (NULL);
	i = 0;
	while (i <= size)
		str[i++] = 0;
	return (str);
}
